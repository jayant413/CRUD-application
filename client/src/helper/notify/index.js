import { toast } from "react-toastify";




export const SuccessMsg = (message) => {
    toast.success(`${message}`, {
        position: "top-center",
        autoClose: 3000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
    });
}

